package co.web.register.to;

public class VoteTO {

	
	private String votingtypeid = "";
	private String uuid = "";
	private String partyid = "";
	private String candidateid = "";
	private String ballotid = "";
	
	
	/**
	 * @return the ballotid
	 */
	public String getBallotid() {
		return ballotid;
	}
	/**
	 * @param ballotid the ballotid to set
	 */
	public void setBallotid(String ballotid) {
		this.ballotid = ballotid;
	}
	/**
	 * @return the votingtypeid
	 */
	public String getVotingtypeid() {
		return votingtypeid;
	}
	/**
	 * @param votingtypeid the votingtypeid to set
	 */
	public void setVotingtypeid(String votingtypeid) {
		this.votingtypeid = votingtypeid;
	}
	/**
	 * @return the uuid
	 */
	public String getUuid() {
		return uuid;
	}
	/**
	 * @param uuid the uuid to set
	 */
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	/**
	 * @return the partyid
	 */
	public String getPartyid() {
		return partyid;
	}
	/**
	 * @param partyid the partyid to set
	 */
	public void setPartyid(String partyid) {
		this.partyid = partyid;
	}
	/**
	 * @return the candidateid
	 */
	public String getCandidateid() {
		return candidateid;
	}
	/**
	 * @param candidateid the candidateid to set
	 */
	public void setCandidateid(String candidateid) {
		this.candidateid = candidateid;
	}

	
	
}
